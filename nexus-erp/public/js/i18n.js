// ─── Estado global ────────────────────────────────────
let _currentLang = localStorage.getItem('erp_lang') || 'pt';

// ─── Função principal de tradução ─────────────────────
function t(key) {
  const dict = I18N[_currentLang] || I18N['pt'];
  return dict[key] !== undefined ? dict[key] : (I18N['pt'][key] !== undefined ? I18N['pt'][key] : key);
}

function getLang()    { return _currentLang; }

function setLang(lang) {
  if (!I18N[lang]) return;
  _currentLang = lang;
  localStorage.setItem('erp_lang', lang);
  _applyI18nDOM();
  // Atualiza UI do topbar (flag/label) se app.js já carregou
  if (typeof _updateLangUI === 'function') _updateLangUI();
}

// ─── Aplica traduções no DOM (elementos com data-i18n) ─
function _applyI18nDOM() {
  document.documentElement.lang = _currentLang === 'pt' ? 'pt-BR' : _currentLang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' && el.type !== 'button' && el.type !== 'submit') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-ph'));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.getAttribute('data-i18n-title'));
  });
  // Atualiza title da página (empresa é setado pelo multi_empresa.js)
  const empName = typeof getEmpresaAtiva === 'function'
    ? (getEmpresaAtiva()?.nome_fantasia || getEmpresaAtiva()?.razao_social || '')
    : '';
  document.title = empName ? `${empName} – ${t('sys_name')}` : t('sys_name');

  // Dispara evento para módulos reagirem à mudança
  window.dispatchEvent(new CustomEvent('langChanged', { detail: { lang: _currentLang } }));
}

// ─── Inicializa ao carregar o DOM ─────────────────────
document.addEventListener('DOMContentLoaded', function() {
  _applyI18nDOM();
});
